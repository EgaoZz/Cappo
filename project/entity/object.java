package project.entity;

class object {
    private int x,y;

    public object(int x,int y){
        this.x=x;
        this.y=y;
    }
    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
